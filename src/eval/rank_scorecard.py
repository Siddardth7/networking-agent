"""
src/eval/rank_scorecard.py
Ranking-validation scorecard (issue #12): does the referral-likelihood ranker
(#11, src/agents/ranker.py) put the right people on top?

Ranking quality is about ORDER, not classification accuracy, so the metrics are:

  - pairwise concordance — over every pair of contacts whose gold help-tier
    differs, the fraction the ranker orders the same way (higher tier → higher
    score). This is the core "does the order agree with judgment" number.
  - tier inversions — a strictly worse target (lower gold tier) scoring ABOVE a
    better one. These are the real failures; each is listed.
  - top-K precision — of the top K ranked (K = number of HIGH-tier contacts),
    how many are actually HIGH. The acceptance bar: "top-ranked = highest help."

The ranker is deterministic and offline, so this whole module is unit-tested —
no network, no `# pragma: no cover`. The live doc is generated by running
``python -m src.eval.rank_scorecard`` against the labeled set.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from enum import IntEnum
from itertools import combinations
from pathlib import Path

from pydantic import BaseModel, field_validator

from src.agents.ranker import rank_contact
from src.core.schemas import ContactCandidate, FocusArea, Persona

__all__ = [
    "HelpTier",
    "LabeledRankContact",
    "RankScorecard",
    "load_labeled_set",
    "score_ranking",
    "format_scorecard",
]

_LABELED_SET_PATH = Path(__file__).with_name("rank_labeled_set.json")


class HelpTier(IntEnum):
    """Gold human judgment of referral help-likelihood. Ordinal — HIGH > MED > LOW."""

    LOW = 1
    MED = 2
    HIGH = 3


class LabeledRankContact(BaseModel):
    """One gold-tiered contact: the signals the ranker sees + the expected tier."""

    full_name: str
    expected_tier: HelpTier
    note: str | None = None
    persona: Persona | None = None
    focus_area: FocusArea | None = None
    alumni_confirmed: bool | None = None
    connection_degree: str | None = None
    snippet: str | None = None
    email: str | None = None
    linkedin_url: str | None = None

    @field_validator("expected_tier", mode="before")
    @classmethod
    def _tier_from_name(cls, v):
        # Accept the readable tier name ("HIGH") from JSON, not just the ordinal.
        return HelpTier[v] if isinstance(v, str) else v

    def to_candidate(self) -> ContactCandidate:
        return ContactCandidate(
            full_name=self.full_name,
            company_slug="eval",
            persona=self.persona,
            focus_area=self.focus_area,
            alumni_confirmed=self.alumni_confirmed,
            connection_degree=self.connection_degree,
            snippet=self.snippet,
            email=self.email,
            linkedin_url=self.linkedin_url,
        )


@dataclass
class ScoredContact:
    name: str
    tier: HelpTier
    score: int
    reasons: str


@dataclass
class RankScorecard:
    scored: list[ScoredContact]  # ranked best-first
    concordant_pairs: int
    differing_pairs: int
    inversions: list[tuple[ScoredContact, ScoredContact]]  # (lower_tier, higher_tier)
    tier_ranges: dict[str, tuple[int, int]] = field(default_factory=dict)
    top_k: int = 0
    top_k_high: int = 0

    @property
    def n(self) -> int:
        return len(self.scored)

    @property
    def concordance(self) -> float:
        return self.concordant_pairs / self.differing_pairs if self.differing_pairs else 1.0

    @property
    def top_k_precision(self) -> float:
        return self.top_k_high / self.top_k if self.top_k else 1.0

    @property
    def verdict(self) -> str:
        # "top-ranked = highest-help-likelihood": no strictly-worse target above a
        # better one, and the top K are all HIGH.
        if not self.inversions and self.top_k_precision == 1.0:
            return "PASS"
        return "REVIEW — ordering does not fully match gold tiers"

    def render_markdown(self) -> str:
        return format_scorecard(self)


def load_labeled_set(path: str | Path | None = None) -> list[LabeledRankContact]:
    """Load and validate the labeled set (tiers/enums checked on load)."""
    p = Path(path) if path else _LABELED_SET_PATH
    raw = json.loads(p.read_text())
    return [LabeledRankContact(**row) for row in raw]


def score_ranking(labeled: list[LabeledRankContact]) -> RankScorecard:
    """Run the ranker over the labeled set and measure ordering quality. Pure."""
    scored = [
        ScoredContact(
            name=lc.full_name,
            tier=lc.expected_tier,
            score=(rs := rank_contact(lc.to_candidate())).total,
            reasons=rs.summary(),
        )
        for lc in labeled
    ]
    scored.sort(key=lambda s: s.score, reverse=True)

    concordant = differing = 0
    inversions: list[tuple[ScoredContact, ScoredContact]] = []
    for a, b in combinations(scored, 2):
        if a.tier == b.tier:
            continue
        differing += 1
        higher, lower = (a, b) if a.tier > b.tier else (b, a)
        if higher.score > lower.score:
            concordant += 1
        elif lower.score > higher.score:  # strictly worse target outranks a better one
            inversions.append((lower, higher))
        # equal score across tiers: neither concordant nor an inversion (a wash)

    tier_ranges: dict[str, tuple[int, int]] = {}
    for tier in HelpTier:
        vals = [s.score for s in scored if s.tier == tier]
        if vals:
            tier_ranges[tier.name] = (min(vals), max(vals))

    top_k = sum(1 for s in scored if s.tier == HelpTier.HIGH)
    top_k_high = sum(1 for s in scored[:top_k] if s.tier == HelpTier.HIGH)

    return RankScorecard(
        scored=scored,
        concordant_pairs=concordant,
        differing_pairs=differing,
        inversions=inversions,
        tier_ranges=tier_ranges,
        top_k=top_k,
        top_k_high=top_k_high,
    )


def format_scorecard(card: RankScorecard) -> str:
    lines = [
        f"## Ranking validation scorecard (n={card.n})",
        "",
        f"- **Verdict: {card.verdict}**",
        f"- Pairwise concordance: **{card.concordance:.0%}** "
        f"({card.concordant_pairs}/{card.differing_pairs} tier-differing pairs ordered correctly)",
        f"- Top-{card.top_k} precision: **{card.top_k_precision:.0%}** "
        f"({card.top_k_high}/{card.top_k} top-ranked are HIGH tier)",
        f"- Tier inversions (worse target ranked above better): **{len(card.inversions)}**",
        "",
        "### Score range by gold tier",
        "",
        "| tier | score range |",
        "|---|---|",
    ]
    for tier in (HelpTier.HIGH, HelpTier.MED, HelpTier.LOW):
        rng = card.tier_ranges.get(tier.name)
        if rng:
            lines.append(f"| {tier.name} | {rng[0]}–{rng[1]} |")
    if card.inversions:
        lines += [
            "",
            "### Inversions",
            "",
            "| worse (lower tier) | scored | better (higher tier) | scored |",
            "|---|---:|---|---:|",
        ]
        for lower, higher in card.inversions:
            lines.append(
                f"| {lower.name} ({lower.tier.name}) | {lower.score} "
                f"| {higher.name} ({higher.tier.name}) | {higher.score} |"
            )
    lines += [
        "",
        "### Ranked order",
        "",
        "| # | contact | gold tier | score | why |",
        "|---:|---|---|---:|---|",
    ]
    for i, s in enumerate(card.scored, start=1):
        lines.append(f"| {i} | {s.name} | {s.tier.name} | {s.score} | {s.reasons} |")
    return "\n".join(lines) + "\n"


if __name__ == "__main__":  # pragma: no cover - convenience doc generator
    print(format_scorecard(score_ranking(load_labeled_set())))
